var gulp = require('gulp'),
    replace = require('gulp-replace');

gulp.task('release', function(){
  gulp.src(['**/*','!./gulpfile.js','!./lib/images/**/*','!./**/*.png','!./**/css/**/*','!/**/*.ico'])
    //VRCM 192.168.0.208环境转移192.168.0.15环境
    .pipe(replace(/http:\\{2}\/\\{2}\/192\\{2}.168\\{2}.0\\{2}.208:7777/gm,'http:\\\\/\\\\/192\\\\.168\\\\.0\\\\.15:8322'))
    .pipe(replace(/192\.168\.0\.208:7777/gm,'192.168.0.15:8322'))
    .pipe(replace(/192\.168\.0\.208/gm,'192.168.0.15'))
    .pipe(replace(/=192\\{2}.168\\{2}.0\\{2}.208/gm,'=192\\\\.168\\\\.0\\\\.15'))
    //大数据 192.168.1.174环境转移192.168.1.202环境
    .pipe(replace(/http:\\{2}\/\\{2}\/192\\{2}.168\\{2}.1\\{2}.174:8080/gm,'http:\\\\/\\\\/192\\\\.168\\\\.1\\\\.202'))
    .pipe(replace(/192\.168\.1\.174:8080/gm,'192.168.1.202'))
    .pipe(gulp.dest('./'));
});

